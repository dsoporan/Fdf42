#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int nn;

	i = 0;
	nn = 0;
	sign = 1;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-')
	{
		i++;
		sign = -1;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		nn = nn * 10 + (str[i] - '0');
		i++;
	}
	return (nn * sign);
}

#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;
	char	*s1;

	i = 0;
	s1 = (char *)src;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
